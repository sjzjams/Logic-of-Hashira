/// V1.1 升级：根据识别置信度返回视觉特效强度。
///
/// 设计原则:
/// - 置信度 0.0 ~ 0.4 (低 / 不可信):动效偏温和,提醒用户"模型不太确定";
/// - 置信度 0.4 ~ 0.8 (中等):标准动效,体感专业;
/// - 置信度 0.8 ~ 1.0 (高):增强动效,营造"高确信食物被点亮"的仪式感。
///
/// 返回值范围:0.7 (最温和) ~ 1.2 (最强)。
double edgeEffectIntensityForConfidence(double confidence) {
  // 强制限制在 [0,1],避免上游传入 1.3 之类的脏数据。
  final double c = confidence.clamp(0.0, 1.0);
  if (c <= 0.4) {
    return 0.7;
  }
  if (c <= 0.8) {
    // 0.4 -> 0.7 渐变;0.8 -> 1.0
    final double t = (c - 0.4) / 0.4;
    return 0.7 + 0.3 * t;
  }
  // 0.8 -> 1.0;1.0 -> 1.2
  final double t = (c - 0.8) / 0.2;
  return 1.0 + 0.2 * t;
}
